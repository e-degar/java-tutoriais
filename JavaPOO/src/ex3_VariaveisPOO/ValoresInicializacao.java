package ex3_VariaveisPOO;

// O que acontece que eu não atribuir um valor à uma variável global?

public class ValoresInicializacao {

	// Valores inteiros são inicializados com 0
	byte a;
	short b;
	int c;
	long d;

	// Valores decimais são inivializados com 0.0
	float e;
	double f;

	// Variáveis char são inicializadas com o caractere blank -> \u0000
	char g;

	// Variáveis boolean são inicializadas com false
	boolean h;

	// Objetos acessados por referência são inicializados com null
	String i;
}